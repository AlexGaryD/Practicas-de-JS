#Extraer un substring de una cadena con Phyton
frase = "One Piece es un anime muy bueno"
#Extraer un substring de una cadena con Phyton
# Definimos los índices de inicio y fin del substring   
inicial = 4
#Definimos el índice de inicio del substring
final = 10
#Definimos el índice de fin del substring
palabra = frase[inicial:final]
# Imprimimos el substring extraído
print("La palabra es:", palabra)

